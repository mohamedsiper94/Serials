package com.example.myapplication;

public class login  {
    String username;
    String password;

    public login() {
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public login(String username, String password) {
        this.username = username;
        this.password = password;

    }
}
